import { existsSync, mkdirSync, writeFileSync } from 'fs'
import { join, resolve } from 'path'

import { write } from '@/utils/file'

import { mergeClaudeMcpJson, mergeClaudeSettingsJson, mergeClaudeSettingsLocalJson } from './mcp-merge'
import { appendGitignore, slugify, writeAgentFile } from './scaffold-utils'
import {
  agentBuilder,
  agentExplorer,
  agentLead,
  agentReviewer,
  agentsMd,
  claudeMd,
  featureListJson,
  HEALTH_SH,
  translateFrontmatterForClaudeCode,
} from './templates'

import type { Materializer } from './index'
import type { HarnessConfig, Provider, ScaffoldOptions } from '@/types'

export class ClaudeCodeMaterializer implements Materializer {
  async scaffold(config: HarnessConfig, opts: ScaffoldOptions): Promise<void> {
    const { cwd } = opts

    // AGENTS.md and CLAUDE.md — always overwrite (generated from config)
    write(cwd, 'AGENTS.md', agentsMd(config))
    write(cwd, 'CLAUDE.md', claudeMd(config))

    // health.sh — only create if it doesn't exist
    if (!existsSync(join(cwd, 'health.sh'))) {
      write(cwd, 'health.sh', HEALTH_SH, 0o755)
    }

    // .harness/feature_list.json
    const tasks = opts.firstTask ? [{ slug: slugify(opts.firstTask.title), ...opts.firstTask }] : []
    write(cwd, 'feature_list.json', featureListJson(tasks))

    // .harness/current.md placeholder
    if (!existsSync(join(cwd, config.storage.markdownFallback.path))) {
      write(
        cwd,
        config.storage.markdownFallback.path,
        `<!-- AUTO-GENERATED by agent-harness-kit — DO NOT EDIT MANUALLY -->\n<!-- Run ahk status to refresh -->\n\n# Current Session\n\nNo tasks in progress.\n`
      )
    }

    // .claude/agents/ — skip files the dev may have customized
    const projectName = config.project.name
    const allowedPaths = (config.agents.explorer.allowedPaths ?? []).join(', ')
    const writablePaths = (config.agents.builder.writablePaths ?? []).join(', ')
    writeAgentFile(cwd, '.claude/agents/lead.md', translateFrontmatterForClaudeCode(agentLead({ projectName }), 'lead'))
    writeAgentFile(cwd, '.claude/agents/explorer.md', translateFrontmatterForClaudeCode(agentExplorer({ projectName, allowedPaths }), 'explorer'))
    writeAgentFile(cwd, '.claude/agents/builder.md', translateFrontmatterForClaudeCode(agentBuilder({ projectName, writablePaths }), 'builder'))
    writeAgentFile(cwd, '.claude/agents/reviewer.md', translateFrontmatterForClaudeCode(agentReviewer({ projectName }), 'reviewer'))

    // .mcp.json — MERGE, never overwrite whole file
    mergeClaudeMcpJson(join(cwd, '.mcp.json'), config.tools.mcp.port)
    // .claude/settings.json — set `agent: "lead"` (the official Claude Code default-agent field)
    mergeClaudeSettingsJson(join(cwd, '.claude/settings.json'))
    // .claude/settings.local.json — merge MCP tool permissions
    mergeClaudeSettingsLocalJson(join(cwd, '.claude/settings.local.json'))

    // .gitignore additions
    appendGitignore(cwd)
  }

  async build(config: HarnessConfig, cwd: string): Promise<void> {
    const write = (relPath: string, content: string) => {
      const abs = join(cwd, relPath)
      mkdirSync(resolve(abs, '..'), { recursive: true })
      writeFileSync(abs, content, 'utf8')
    }

    // build always regenerates AGENTS.md and CLAUDE.md (derived from config)
    write('AGENTS.md', agentsMd(config))
    write('CLAUDE.md', claudeMd(config))

    // Agent files: skip if customized, write if missing
    const projectName = config.project.name
    const allowedPaths = (config.agents.explorer.allowedPaths ?? []).join(', ')
    const writablePaths = (config.agents.builder.writablePaths ?? []).join(', ')
    writeAgentFile(cwd, '.claude/agents/lead.md', translateFrontmatterForClaudeCode(agentLead({ projectName }), 'lead'))
    writeAgentFile(cwd, '.claude/agents/explorer.md', translateFrontmatterForClaudeCode(agentExplorer({ projectName, allowedPaths }), 'explorer'))
    writeAgentFile(cwd, '.claude/agents/builder.md', translateFrontmatterForClaudeCode(agentBuilder({ projectName, writablePaths }), 'builder'))
    writeAgentFile(cwd, '.claude/agents/reviewer.md', translateFrontmatterForClaudeCode(agentReviewer({ projectName }), 'reviewer'))

    // MCP config: always merge
    mergeClaudeMcpJson(join(cwd, '.mcp.json'), config.tools.mcp.port)
    mergeClaudeSettingsJson(join(cwd, '.claude/settings.json'))
    mergeClaudeSettingsLocalJson(join(cwd, '.claude/settings.local.json'))
  }

  async migrate(config: HarnessConfig, _to: Provider, _cwd: string): Promise<void> {
    void config
    // Migration from claude-code is handled by the target materializer
  }
}
